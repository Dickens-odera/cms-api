    "Access-Control-Allow-Origin": "*",
};

let body = {
    "name": "aut",
    "email": "doloribus",
    "password": "illum",
    "confirm_password": "molestias"
}

fetch(url, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
}).then(response =&gt; response.json());</code></pre>

